ONGC Academy has an experienced core and visiting external faculty from in-house, industry and top national institutions who possess specialization, experience, institutional affiliation and temperament.The Pedagogy is interactive and participative and the methods include lectures, cases, seminars, group discussions, business games, role plays, simulation exercises, structured and unstructured group work and field visits. Functional areas are discussed in the context of opportunities for internationalizing business and enhancing the organizations responsiveness to the rapidly changing technology and market forces in the emerging global scenario. Eminent academicians, policy makers & senior executives deliver extensive lectures to the faculty and the participants.</p>



