<p style="margin-left: 50px; margin-right: 90px; color: #000000; font-size: 17px;margin-top:70px">
ONGC Academy is located in the lush green environment of the Himalayas at Dehra Dun. Known previously as Institute of Management Development (IMD), it was formally renamed as ONGC Academy on November 2, 2003 by C&MD Mr. Subir Raha. It is ONGC's premier nodal agency for training and developing human resources. The Institute emerged out of SWOT analysis carried out by the organisation in 1982.Designing parameters for measuring performance of human resources, succession planning, mapping of individual relations scenario, work climate and work culture analysis and managing change are some of the areas of research related to management development. To serve this purpose, the Academy is committed to excellence in the cause of HRD and of the availability of appropriate systems and procedures with a view to ensure managerial effectiveness, quality and productivity in E&P Sector. ONGC Academy is also responsible for coordinating training/seminars for ONGC executives abroad.</p>


