Training programmes are as per the specific requirements catering to the fresh graduate trainees, middle and senior level corporate executives. Refresher, awareness and exposure courses in the field of geo-science, production, and reservoir engineering, emerging technologies and managerial aspects for the national international oil companies are conducted regularly. Programme on joint ventures, negotiations, price-risk analysis, cost reduction, advanced petroleum management etc. are added features of the training curriculum.</p>



