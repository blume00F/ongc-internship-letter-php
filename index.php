   <p style="margin-left: 50px; color: #000000; font-size: 14px; margin-right: 40px;margin-top:40px"><b>GEOPIC</b></p>
  <p style="margin-left: 50px; color: #000000; font-size: 14px; margin-right: 40px;">Geodata Processing and Interpretation Centre established in 1987 at ONGC, Dehradun provides solutions to complex E&P problems by integrating seismic with other geoscientific data. Land and marine seismic data of ONGC of Indian & Foreign basins is processed and interpreted synergistically at this centre which has world class state-of-the-art infrastructure backed by specialists.Since inception, GEOPIC transformed itself into a Centre for Excellence and today is one of the premier institutes of ONGC.</p>

   <p style="margin-left: 50px; color: #000000; font-size: 14px; margin-right: 40px;"><b>KDMIPE</b></p>
  <p style="margin-left: 50px; color: #000000; font-size: 14px; margin-right: 40px;">Keshava Deva Malaviya Institute of Petroleum Exploration (KDMIPE) is located in picturesque valley of Dehradun in the state of Uttrakhand. It was founded in 1962 with an objective to provide geo-scientific back up to the exploratory efforts of India's National oil company, ONGC.
KDMIPE has been an ISO: 9001:2000 certified institute from 3 December 2004 to 03 December 2007. To achieve the highest standard of Quality, Health, Safety and Environment, KDMIPE has strived to get QHSE certificate and the same was awarded to KDMIPE on 13 June 2008.</p>

  <p style="margin-left: 50px; color: #000000; font-size: 14px; margin-right: 40px;"><b>IDT</b></p>
  <p style="margin-left: 50px; color: #000000; font-size: 14px; margin-right: 40px;"> The Institute of Drilling Technology (IDT) was set up in 1978 at Dehradun. Over the years, the Institute has emerged as a premier R&D centre in South East Asia. Institute of Drilling Technology (IDT) provides its techno-economic expertise & solutions to various field problems faced by various services of ONGC with the ultimate objective to promote cost effective E&P activities of the company.  </p>

  <p style="margin-left: 50px; color: #000000; font-size: 14px; margin-right: 40px;"><b>IEOT</b></p>
  <p style="margin-left: 50px; color: #000000; font-size: 14px; margin-right: 40px;">The Institute of Engineering and Ocean Technology (IEOT) was founded in 1983 for innovation, development and acceleration of the future plans of ONGC to achieve self- reliance in technology. The Institute has developed expertise in core fields of Geotechnical Engineering, Structural Engineering, Risk & Reliability Engineering, Materials & Corrosion Engineering and Alternate Energy section was set up in 2012.</p>

  <p style="margin-left: 50px; color: #000000; font-size: 14px; margin-right: 40px;"><b>IOGPT</b></p>
  <p style="margin-left: 50px; color: #000000; font-size: 14px; margin-right: 40px;margin-bottom:35px">The Institute of Oil & Gas Production Technology (IOGPT) was established in 1984 with assistance from United Nations Development Program (UNDP) and Asian Development Bank (ADB) to meet the production and processing related technological requirement of Oil and Gas fields and processing plants. The institute is located at the Technology Park in Panvel, Navi Mumbai.
  IOGPT is the first Institute in the country to provide an integrated R&D support to the entire spectrum of Oil and Gas production, which begins at the well bore and culminates at the consumer point.</p>

